"""Modulo de practica para sumar"""

def sumar(par_1 =0,par_2=0):
    """Mdulo para sumar, modulo de practica"""

    result = par_1 + par_2
    print(f"El resultado suma es -> {result}")

def restar(par_1,par_2):
    """Funcion para restar dos numeros"""
    result = par_1 - par_2
    print(f"Resta -> {result}")

def multiplication(par_1,par_2):
    """Function to multuply 2 numbers"""

    result = par_1 * par_2
    print(f"Multiplicacion -> {result}")